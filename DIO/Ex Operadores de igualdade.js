// Operadores de igualdade 
// Igualdade restrita (mais recomendado)
console.log(1===1); //Compara valor e tipo da variável
console.log('1' == 1); 

// Igualdade solta (não recomendado)
console.log(1 == 1); //Ignora o tipo e compara apenas os valores
console.log('1' == 1); //Caso os tipos sejam diferentes ele converte pare que os tipos fiquem iguais